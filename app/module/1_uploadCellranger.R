        fluidRow(
          column(6, numericInput(ns("min_count"), "Min Counts (nCount_RNA)", value = 0, min = 0, step = 100)),
          column(6, numericInput(ns("max_count"), "Max Counts (nCount_RNA)", value = 100000, min = 0, step = 100))

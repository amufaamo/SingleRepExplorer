          plotOutput(ns("qc_vln_plot"), height = "350px"),
          tags$div(style = "margin-top: 20px;"),
          plotOutput(ns("qc_scatter_plot"), height = "350px")
        )
      )
    )
  )
}

# ==============================================================================
# Server ロジック (修正版)
# ==============================================================================
uploadCellrangerServer <- function(id, myReactives) {
  moduleServer(id, function(input, output, session) {
    ns <- session$ns

    output$h5_status_ui <- renderUI({ req(input$h5); tags$p("✅ Uploaded!", style = "color: green;") })
    output$rds_status_ui <- renderUI({ req(input$rds); tags$p("✅ Uploaded!", style = "color: green;") })
    output$tcr_status_ui <- renderUI({ req(input$tcr); tags$p("✅ Uploaded!", style = "color: green;") })
    output$bcr_status_ui <- renderUI({ req(input$bcr); tags$p("✅ Uploaded!", style = "color: green;") })

    # -- Dynamic QC Preview Logic --
    # Notice we store it in myReactives to share with the main pipeline later to skip reloading!
    observeEvent(input$h5, {
        myReactives$is_demo <- FALSE
        req(input$h5$datapath)
        shinyjs::show("status_message")
        shinyjs::html(id = "status_message", html = "<p style='color: blue;'>Loading .h5 file for QC preview. This may take a moment...</p>")
        tryCatch({
          h5_data <- Seurat::Read10X_h5(input$h5$datapath)
          if (is.list(h5_data) && !is.data.frame(h5_data) && ("Gene Expression" %in% names(h5_data))) {
            h5_data <- h5_data[["Gene Expression"]]
          }
          raw_so <- Seurat::CreateSeuratObject(counts = h5_data, project = "QC_Preview")
          raw_so[["percent.mt"]] <- Seurat::PercentageFeatureSet(raw_so, pattern = "^MT-")
          
          # Assign to myReactives so we can render plots immediately and re-use later
          myReactives$raw_qc_seurat <- raw_so
          
          # Dynamically update the UI inputs to match the actual data boundaries!
          updateNumericInput(session, "min_features", value = min(raw_so$nFeature_RNA))
          updateNumericInput(session, "max_features", value = max(raw_so$nFeature_RNA))
          updateNumericInput(session, "min_count", value = min(raw_so$nCount_RNA))
          updateNumericInput(session, "max_count", value = max(raw_so$nCount_RNA))
          updateNumericInput(session, "max_mt", value = ceiling(max(raw_so$percent.mt)))
          
        }, error = function(e) {
          showNotification(paste("Error loading .h5:", e$message), type = "error")
        })
        shinyjs::hide("status_message")
    })
    
    # Observe Demo Data click to populate QC plots
    observeEvent(input$demo_run, {
        demo_h5 <- "example/230405_ruft_hcw_vaccine_merge_3000.h5"
        if(file.exists(demo_h5)) {
            shinyjs::show("status_message")
            shinyjs::html(id = "status_message", html = "<p style='color: blue;'>Loading Demo Data for QC preview... Please tune the parameters and click Run.</p>")
            
            # Setup demo state flag
            myReactives$is_demo <- TRUE
            
            h5_data <- Seurat::Read10X_h5(demo_h5)
            if (is.list(h5_data) && !is.data.frame(h5_data) && ("Gene Expression" %in% names(h5_data))) {
              h5_data <- h5_data[["Gene Expression"]]
            }
            raw_so <- Seurat::CreateSeuratObject(counts = h5_data, project = "QC_Preview_Demo")
            raw_so[["percent.mt"]] <- Seurat::PercentageFeatureSet(raw_so, pattern = "^MT-")
            myReactives$raw_qc_seurat <- raw_so
            
            # Dynamically update the UI inputs for demo data boundaries
            updateNumericInput(session, "min_features", value = min(raw_so$nFeature_RNA))
            updateNumericInput(session, "max_features", value = max(raw_so$nFeature_RNA))
            updateNumericInput(session, "min_count", value = min(raw_so$nCount_RNA))
            updateNumericInput(session, "max_count", value = max(raw_so$nCount_RNA))
            updateNumericInput(session, "max_mt", value = ceiling(max(raw_so$percent.mt)))
            
            updateRadioButtons(session, "datatype", selected = "h5")
            
            shinyjs::delay(4000, shinyjs::hide("status_message"))
        }
    })
    
    # Render Dynamic cell count UI
    output$qc_cell_count_ui <- renderUI({
      req(myReactives$raw_qc_seurat)
      so <- myReactives$raw_qc_seurat
      original_cells <- ncol(so)
      
      # Calculate remaining cells dynamically based on inputs
      filtered_cells <- sum(
        so$nFeature_RNA > input$min_features & 
        so$nFeature_RNA < input$max_features & 
        so$nCount_RNA > input$min_count &
        so$nCount_RNA < input$max_count &
        so$percent.mt < input$max_mt
      )
      
      tags$div(
        style = "margin-top: 10px; margin-bottom: 20px; padding: 15px; background-color: #e6f7ff; border-left: 5px solid #1890ff; border-radius: 4px;",
        tags$span(style = "font-size: 1.1em; font-weight: bold;", "Estimated Cells Remaining: "),
        tags$span(style = "color: #1890ff; font-size: 1.3em; font-weight: bold;", as.character(filtered_cells)),
        tags$span(style = "color: #555; font-size: 1.1em;", paste0(" / ", original_cells, " (", round(filtered_cells/original_cells*100, 1), "%)"))
      )
    })

    # Render QC Violin Plot
    output$qc_vln_plot <- renderPlot({
      req(myReactives$raw_qc_seurat)
      so <- myReactives$raw_qc_seurat
      
      p1 <- Seurat::VlnPlot(so, features = "nFeature_RNA", pt.size = 0.1) +
            ggplot2::geom_hline(yintercept = input$min_features, col = "red", linetype = "dashed", linewidth=1) + 
            ggplot2::geom_hline(yintercept = input$max_features, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::theme(legend.position = "none")
            
      p2 <- Seurat::VlnPlot(so, features = "nCount_RNA", pt.size = 0.1) +
            ggplot2::geom_hline(yintercept = input$min_count, col = "red", linetype = "dashed", linewidth=1) + 
            ggplot2::geom_hline(yintercept = input$max_count, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::theme(legend.position = "none")
            
      p3 <- Seurat::VlnPlot(so, features = "percent.mt", pt.size = 0.1) +
            ggplot2::geom_hline(yintercept = input$max_mt, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::theme(legend.position = "none")
      
      return(p1 | p2 | p3)
    })
    
    # Render QC Scatter Plot
    output$qc_scatter_plot <- renderPlot({
      req(myReactives$raw_qc_seurat)
      so <- myReactives$raw_qc_seurat
      
      p1 <- Seurat::FeatureScatter(so, feature1 = "nCount_RNA", feature2 = "percent.mt") +
            ggplot2::geom_hline(yintercept = input$max_mt, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::geom_vline(xintercept = input$min_count, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::geom_vline(xintercept = input$max_count, col = "red", linetype = "dashed", linewidth=1)
            
      p2 <- Seurat::FeatureScatter(so, feature1 = "nCount_RNA", feature2 = "nFeature_RNA") +
            ggplot2::geom_hline(yintercept = input$min_features, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::geom_hline(yintercept = input$max_features, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::geom_vline(xintercept = input$min_count, col = "red", linetype = "dashed", linewidth=1) +
            ggplot2::geom_vline(xintercept = input$max_count, col = "red", linetype = "dashed", linewidth=1)
            
      return(p1 + p2)
    })

    # --- 共通解析ロジック関数 ---
    run_core_analysis_pipeline <- function() {
          if (myReactives$datatype == "h5") {
            # (2) .h5からSeuratオブジェクト作成
            incProgress(0.1, detail = "Loading .h5 file...")
            tmp_reactives <- h5_to_seurat_object(myReactives)
            for(n in names(tmp_reactives)) myReactives[[n]] <- tmp_reactives[[n]]
  
            # (3) Seurat標準解析 (Normalization, PCA, UMAP, etc.)
            incProgress(0.2, detail = "Running basic Seurat processing...")
            # Apply quality control filters set from UI
            tmp_reactives <- run_seurat_object(myReactives, 
                                               filter_min_feat = input$min_features, 
                                               filter_max_feat = input$max_features, 
                                               filter_min_count = input$min_count,
                                               filter_max_count = input$max_count,
                                               filter_max_mt   = input$max_mt,
                                               run_harmony     = input$run_harmony) 
            for(n in names(tmp_reactives)) myReactives[[n]] <- tmp_reactives[[n]]
  
            # (4) Automated Cell Annotation
            if (input$annotation_method == "sctype") {
              incProgress(0.1, detail = "Running scType cell typing...")
              message("--- Running scType analysis for tissue: ", input$sctype_tissue, " ---")
              myReactives$seurat_object <- run_sctype_and_update_seurat(myReactives$seurat_object, tissue = input$sctype_tissue)
            } else if (input$annotation_method == "singler") {
              incProgress(0.1, detail = "Running SingleR cell typing...")
              message("--- Running SingleR analysis using: ", input$singler_ref, " ---")
              myReactives$seurat_object <- run_singler_and_update_seurat(myReactives$seurat_object, ref_name = input$singler_ref)
            }
            myReactives$meta.data <- myReactives$seurat_object@meta.data
          } else if (myReactives$datatype == "rds") {
            # (2-4) ユーザーアップロードのSeuratオブジェクトを読み込む (前処理スキップ)
            incProgress(0.4, detail = "Loading pre-processed Seurat object...")
            message("--- Loading pre-processed RDS. Skipping Seurat normalizations and scType. ---")
            myReactives$seurat_object <- readRDS(myReactives$rds_path)
            
            # 安全のための最低限のメタデータ整理
            if(is.null(rownames(myReactives$seurat_object@meta.data))) {
              rownames(myReactives$seurat_object@meta.data) <- colnames(myReactives$seurat_object)
            }
            # サンプル列がなければ作成する
            if(!"sample" %in% colnames(myReactives$seurat_object@meta.data)) {
               myReactives$seurat_object$sample <- "Sample1"
            }
            myReactives$meta.data <- myReactives$seurat_object@meta.data
          }

          # (5) TCRデータ処理
          if (!is.null(myReactives$tcr_path)) {
            incProgress(0.1, detail = "Processing TCR data...")
            req(myReactives$seurat_object)
            myReactives$tcr_df <- tcr_csv_to_dataframe(myReactives$tcr_path)
            
            # Seuratメタデータとマージ
            seurat_barcodes <- rownames(myReactives$seurat_object@meta.data)
            myReactives$tcr_df <- myReactives$tcr_df %>%
              dplyr::filter(barcode %in% seurat_barcodes)
            
            metadata <- myReactives$seurat_object@meta.data %>%
              tibble::rownames_to_column(var = "barcode") %>% 
              dplyr::select(any_of(c("sample", "seurat_clusters")), barcode)
            myReactives$tcr_df <- dplyr::left_join(myReactives$tcr_df, metadata, by = "barcode")
            
            # 'tcr_status' 列をメタデータに追加
            message("--- Adding 'tcr_status' column to Seurat metadata ---")
            seurat_barcodes <- rownames(myReactives$seurat_object@meta.data)
            tcr_barcodes <- myReactives$tcr_df$barcode
            myReactives$seurat_object$tcr_status <- ifelse(seurat_barcodes %in% tcr_barcodes, "TCR positive", "TCR negative")
            
            # Extract and add Clone Size and Clone ID as metadata
            if ("raw_clonotype_id" %in% names(myReactives$tcr_df)) {
                clone_counts <- table(myReactives$tcr_df$raw_clonotype_id)
                tcr_sizes <- as.numeric(clone_counts[myReactives$tcr_df$raw_clonotype_id])
                
                size_map <- stats::setNames(tcr_sizes, myReactives$tcr_df$barcode)
                myReactives$seurat_object$TCR_cloneSize <- unname(size_map[seurat_barcodes])
                myReactives$seurat_object$TCR_cloneSize[is.na(myReactives$seurat_object$TCR_cloneSize)] <- 0
                
                id_map <- stats::setNames(myReactives$tcr_df$raw_clonotype_id, myReactives$tcr_df$barcode)
                myReactives$seurat_object$TCR_clonotype_id <- unname(id_map[seurat_barcodes])
                myReactives$seurat_object$TCR_clonotype_id[is.na(myReactives$seurat_object$TCR_clonotype_id)] <- "None"
            }
            
            message("'tcr_status' column added. Summary:")
            print(table(myReactives$seurat_object@meta.data$tcr_status))
          }

          # (6) BCRデータ処理
          if (!is.null(myReactives$bcr_path)) {
            incProgress(0.1, detail = "Processing BCR data...")
            req(myReactives$seurat_object)
            myReactives$bcr_df <- bcr_csv_to_dataframe(myReactives$bcr_path) # 修正: 正しい関数を呼び出す
            
            seurat_barcodes <- rownames(myReactives$seurat_object@meta.data)
            myReactives$bcr_df <- myReactives$bcr_df %>%
              dplyr::filter(barcode %in% seurat_barcodes)
              
            metadata <- myReactives$seurat_object@meta.data %>%
              tibble::rownames_to_column(var = "barcode") %>%
              dplyr::select(any_of(c("sample", "seurat_clusters")), barcode)
            myReactives$bcr_df <- dplyr::left_join(myReactives$bcr_df, metadata, by = "barcode")

            message("--- Adding 'bcr_status' column to Seurat metadata ---")
            seurat_barcodes <- rownames(myReactives$seurat_object@meta.data)
            bcr_barcodes <- myReactives$bcr_df$barcode
            myReactives$seurat_object$bcr_status <- ifelse(seurat_barcodes %in% bcr_barcodes, "BCR positive", "BCR negative")
            
            # Extract and add Clone Size and Clone ID as metadata
            if ("raw_clonotype_id" %in% names(myReactives$bcr_df)) {
                clone_counts <- table(myReactives$bcr_df$raw_clonotype_id)
                bcr_sizes <- as.numeric(clone_counts[myReactives$bcr_df$raw_clonotype_id])
                
                size_map <- stats::setNames(bcr_sizes, myReactives$bcr_df$barcode)
                myReactives$seurat_object$BCR_cloneSize <- unname(size_map[seurat_barcodes])
                myReactives$seurat_object$BCR_cloneSize[is.na(myReactives$seurat_object$BCR_cloneSize)] <- 0
                
                id_map <- stats::setNames(myReactives$bcr_df$raw_clonotype_id, myReactives$bcr_df$barcode)
                myReactives$seurat_object$BCR_clonotype_id <- unname(id_map[seurat_barcodes])
                myReactives$seurat_object$BCR_clonotype_id[is.na(myReactives$seurat_object$BCR_clonotype_id)] <- "None"
            }
            
            message("'bcr_status' column added. Summary:")
            print(table(myReactives$seurat_object@meta.data$bcr_status))
          }
          
          # (7) 解析結果をキャッシュとして保存
          incProgress(0.1, detail = "Finalizing and saving results to cache...")
          # demo_runの場合はキャッシュを上書きしない方が良いかもしれないが、
          # 仕様上「解析結果」は一つなので上書きされる。
          results_to_save <- reactiveValuesToList(myReactives)
          saveRDS(results_to_save, file = "analysis_cache.rds") # パスは共通
          message(paste("--- ✅ Analysis complete. Results saved to analysis_cache.rds ---"))
    }

    # [NOTE] Demo Data Run Event historically ran the pipeline automatically,
    # but has been heavily refactored to just map the demo data so we can use Dynamic QC. 
    # The actual pipeline trigger happens entirely via `input$run` now!
    observeEvent(input$run, {
      # --- 1. キャッシュファイルのパスを定義 ---
      cache_file_path <- "analysis_cache.rds"

      # --- 2. 入力とキャッシュの存在をチェック ---
      # キャッシュがなく、かつ.h5/.rdsファイルもアップロードされていない場合はエラー
      input_missing <- !isTRUE(myReactives$is_demo) && ((input$datatype == "h5" && is.null(input$h5$datapath)) || (input$datatype == "rds" && is.null(input$rds$datapath)))
      
      if (!file.exists(cache_file_path) && input_missing) {
        shinyalert::shinyalert("Input Required", "Please upload the primary input file (.h5 or .rds) for the first analysis.", type = "error")
        return()
      }

      # --- 3. UIの初期化 ---
      shinyjs::hide("download_results")
      shinyjs::disable("run")
      shinyjs::disable("demo_run")
      shinyjs::show("status_message")

      withProgress(message = 'Analysis in progress', value = 0, {
        # --- 4. キャッシュの読み込み、または新規解析の実行 ---
        
        if (file.exists(cache_file_path) && input_missing) {
          # --- [A] キャッシュが存在する場合：読み込んでスキップ ---
          shinyjs::html(id = "status_message", html = paste0("<p style='color: #8A2BE2;'>📁 Found '", cache_file_path, "'. Loading cached analysis results...</p>"))
          incProgress(0.2, detail = "Loading cached data...")

          # キャッシュファイルを読み込む
          cached_data <- readRDS(cache_file_path)

          # myReactivesの各項目を復元
          for (name in names(cached_data)) {
            myReactives[[name]] <- cached_data[[name]]
          }

          incProgress(0.8, detail = "Cached data loaded successfully.")
          message(paste("✅ Successfully loaded all analysis results from", cache_file_path))


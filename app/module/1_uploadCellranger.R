        tags$p("Adjust the criteria to filter out low-quality cells or doublets."),
        fluidRow(
          column(6, numericInput(ns("min_features"), "Min Features (nFeature_RNA)", value = 200, min = 0, step = 50)),
          column(6, numericInput(ns("max_features"), "Max Features (nFeature_RNA)", value = 2500, min = 0, step = 50))

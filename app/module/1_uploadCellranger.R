        )
      ),
      conditionalPanel(
        condition = "input.datatype == 'rds'",
        ns = ns,
        fluidRow(
          column(8, fileInput(ns("rds"), "Choose Seurat .rds file", accept = c(".rds"))),
          column(4, style = "margin-top: 25px;", uiOutput(ns("rds_status_ui")))
        )
      ),
      
      fluidRow(
        column(8, fileInput(ns("tcr"), "2. Choose .tcr file (optional)")),
        column(4, style = "margin-top: 25px;", uiOutput(ns("tcr_status_ui")))
      ),
      fluidRow(
        column(8, fileInput(ns("bcr"), "3. Choose .bcr file (optional)")),
        column(4, style = "margin-top: 25px;", uiOutput(ns("bcr_status_ui")))
      ),
      
      conditionalPanel(
        condition = "input.datatype == 'h5'",
        ns = ns,
        tags$hr(),
        tags$h4("Cell Annotation Settings"),
        tags$p("Choose between marker-based fast annotation (scType) or reference-based accurate annotation (SingleR)."),
        selectInput(ns("annotation_method"), "Annotation Method", 
                    choices = c("scType (Marker-based)" = "sctype", "SingleR (Reference-based)" = "singler"), 
                    selected = "sctype"),
        conditionalPanel(
          condition = sprintf("input['%s'] == 'sctype'", ns("annotation_method")),
          selectInput(ns("sctype_tissue"), "Target Tissue (scType)",
                      choices = c("Immune system", "Pancreas", "Heart", "Brain", "Placenta", "Kidney", "Liver", "Lung", "Muscle", "Intestine"), 
                      selected = "Immune system")
        ),
        conditionalPanel(
          condition = sprintf("input['%s'] == 'singler'", ns("annotation_method")),
          selectInput(ns("singler_ref"), "Reference Dataset (celldex)",
                      choices = list(
                        "Human Primary Cell Atlas (Broad)" = "HumanPrimaryCellAtlasData",
                        "Monaco Immune Data (Immune)" = "MonacoImmuneData",
                        "Database of Immune Cell Expression (DICE)" = "DatabaseImmuneCellExpressionData",
                        "Novershtern Hematopoietic Data" = "NovershternHematopoieticData",
                        "Blueprint Encode Data" = "BlueprintEncodeData",
                        "Mouse RNA-seq Data (Broad)" = "MouseRNAseqData",
                        "ImmGen (Mouse Immune)" = "ImmGenData"
                      ), selected = "HumanPrimaryCellAtlasData")
        ),
        tags$hr(),
